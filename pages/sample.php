<br><b><font size=4>¿Que son las Asesorías?</font></b>
<br>
Son clases impartidas por Asesores Estudiantes, las cuales abarcan ciertas materias incluidas en cada nivel académico del CECyT.
Estas Asesorías pueden abarcar todas las áreas como son:
<ul type=disc>
<li>Básicas
<li>Humanísticas
<li>Tecnológicas
</ul>



<b><font size=4>Objetivo de las Asesorías</font></b><br>
Mejorar el rendimiento académico del alumnado que lleva bajo nivel.


<br><br><b><font size=4>Misión</font></b><br>
La misión de la Asesoría está enfocada principalmente al asesorado, la cual el Asesor Estudiante tiene que:
<ul type=disc>
<li>Aconsejar
<li>Orientar
<li>Informar
<li>Detectar deficiencias y aportar las soluciones
</ul>


<b><font size=4>¿Quiénes son los Asesores Estudiantes?</font></b><br>
Son estudiantes de alto desempeño académico, para desarrollar su servicio social como asesores en materias específicas durante un semestre en el que se ejercen como docentes auxiliares o asesores.
<br>Los asesores estudiantes se encargan de asesorar, orientar y aconsejar  a los compañeros con problemas de aprendizaje o con dudas específicas en ciertas materias.


<br><br><b><font size=4>Elementos Personales de un Asesor Estudiante</font></b><br>
Estos elementos tienen que estar siempre presentes en el Asesor, son clave para tener un resultado positivo en el asesorado, como es:
<ul type=disc>
<li>Accesibilidad
<li>Disponibilidad
<li>Flexibilidad
<li>Credibilidad
<li>Paciencia
</ul>

<b><font size=4>Funciones del asesor</font></b><br>
Apoyar con sus conocimientos y experiencia a los alumnos que tienen dificultades en las materias que se le imparten al asesorado,  con la finalidad de ser la guía de los resultados exitosos deseados.<br>
El Asesor realizará un seguimiento permanente, eficaz y orientado a la optimización del esfuerzo de estudio.

